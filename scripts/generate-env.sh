EOF

chmod 600 .env
echo "✅ Wrote complete .env (Postgres pw, JWT secret, ANON/SERVICE JWTs, dashboard creds)."
echo "   Dashboard: ${DASHBOARD_USERNAME} / ${DASHBOARD_PASSWORD}"
echo "   ⚠  Set TATUM_API_KEY and PAYOUT_* before accepting real payments."
