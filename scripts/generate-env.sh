#!/usr/bin/env bash
# Onion Classifieds — unattended .env generator.
#
# Produces a COMPLETE .env for the full self-hosted Supabase stack + the PHP
# app: random secrets, and correctly-signed HS256 JWTs for ANON_KEY /
# SERVICE_ROLE_KEY (derived from JWT_SECRET). Idempotent: if .env already
# exists it is left untouched unless --force is passed. Requires only bash +
# openssl (no Node, no Supabase CLI) so it runs from cloud-init with nothing
# pre-installed but coreutils.
set -euo pipefail
cd "$(dirname "$0")/.."

FORCE=0
[ "${1:-}" = "--force" ] && FORCE=1

if [ -f .env ] && [ "$FORCE" -ne 1 ]; then
  echo "✅ .env already exists — leaving it (use --force to regenerate)."
  exit 0
fi

rand_hex()  { openssl rand -hex "${1:-32}"; }
# url-safe base64 with padding stripped
b64url() { openssl base64 -e -A | tr '+/' '-_' | tr -d '='; }

# Mint an HS256 JWT for a Supabase role, signed with JWT_SECRET.
jwt() {
  local role="$1" secret="$2" iat exp header payload sig
  iat=$(date +%s)
  exp=$((iat + 3650 * 24 * 3600)) # ~10 years
  header=$(printf '{"alg":"HS256","typ":"JWT"}' | b64url)
  payload=$(printf '{"role":"%s","iss":"supabase","iat":%s,"exp":%s}' "$role" "$iat" "$exp" | b64url)
  sig=$(printf '%s' "$header.$payload" | openssl dgst -sha256 -hmac "$secret" -binary | b64url)
  printf '%s.%s.%s' "$header" "$payload" "$sig"
}

echo "🔐 Generating secrets + Supabase JWT keys…"
POSTGRES_PASSWORD="$(rand_hex 24)"
JWT_SECRET="$(openssl rand -base64 48 | tr -d '=+/' | cut -c1-48)"
APP_SECRET="$(rand_hex 32)"
DASHBOARD_USERNAME="admin"
DASHBOARD_PASSWORD="$(rand_hex 12)"
LOGFLARE_API_KEY="$(rand_hex 32)"
ANON_KEY="$(jwt anon "$JWT_SECRET")"
SERVICE_ROLE_KEY="$(jwt service_role "$JWT_SECRET")"

# Public URL: the .onion (set later by get-onion-address.sh) or a placeholder.
APP_BASE_URL="${APP_BASE_URL:-http://localhost:8080}"

cat > .env <<EOF
