# Regenerate with: ./scripts/generate-env.sh --force

############ Postgres / Supabase core ############
POSTGRES_HOST=db
POSTGRES_PORT=5432
POSTGRES_DB=postgres
POSTGRES_PASSWORD=${POSTGRES_PASSWORD}

JWT_SECRET=${JWT_SECRET}
JWT_EXPIRY=3600
ANON_KEY=${ANON_KEY}
SERVICE_ROLE_KEY=${SERVICE_ROLE_KEY}

############ Supabase Studio / Kong ############
DASHBOARD_USERNAME=${DASHBOARD_USERNAME}
DASHBOARD_PASSWORD=${DASHBOARD_PASSWORD}
STUDIO_DEFAULT_ORGANIZATION="Onion Classifieds"
STUDIO_DEFAULT_PROJECT=OnionClassifieds
STUDIO_PORT=3000
KONG_HTTP_PORT=8000
KONG_HTTPS_PORT=8443
SUPABASE_PUBLIC_URL=http://localhost:8000
API_EXTERNAL_URL=http://localhost:8000
SITE_URL=${APP_BASE_URL}
ADDITIONAL_REDIRECT_URLS=
PGRST_DB_SCHEMAS=public,storage,graphql_public
LOGFLARE_API_KEY=${LOGFLARE_API_KEY}
FUNCTIONS_VERIFY_JWT=false
IMGPROXY_ENABLE_WEBP_DETECTION=true
DOCKER_SOCKET_LOCATION=/var/run/docker.sock

############ GoTrue (auth) — anonymous-account app, email/phone off ############
DISABLE_SIGNUP=false
ENABLE_EMAIL_SIGNUP=false
ENABLE_ANONYMOUS_USERS=true
ENABLE_EMAIL_AUTOCONFIRM=true
ENABLE_PHONE_SIGNUP=false
ENABLE_PHONE_AUTOCONFIRM=false
SMTP_ADMIN_EMAIL=admin@example.com
SMTP_HOST=supabase-mail
SMTP_PORT=2500
SMTP_USER=fake_mail_user
SMTP_PASS=fake_mail_password
SMTP_SENDER_NAME="Onion Classifieds"
MAILER_URLPATHS_INVITE=/auth/v1/verify
MAILER_URLPATHS_CONFIRMATION=/auth/v1/verify
MAILER_URLPATHS_RECOVERY=/auth/v1/verify
MAILER_URLPATHS_EMAIL_CHANGE=/auth/v1/verify

############ PHP application ############
DATABASE_URL=postgresql://postgres:${POSTGRES_PASSWORD}@db:5432/postgres
APP_SECRET=${APP_SECRET}
APP_BASE_URL=${APP_BASE_URL}
APP_DEBUG=false

# Exchange rates (Tatum) — REQUIRED for pricing; set your key before going live.
TATUM_API_URL=https://api.tatum.io/v3
TATUM_API_KEY=your-tatum-api-key

# CryptAPI payout addresses — REQUIRED; replace with your own before going live.
PAYOUT_BTC=your-btc-address
PAYOUT_XMR=your-xmr-address
PAYOUT_ETH=your-eth-address
PAYOUT_SOL=your-sol-address
PAYOUT_DOGE=your-doge-address
PAY_CONF_DEFAULT=1
